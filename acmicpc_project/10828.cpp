#include <iostream>
using namespace std;

int Stack[10001];
int top = -1;

//push X : 정수 X를 스택에 넣는 연산이다.
//pop : 스택에서 가장 위에 있는 정수를 빼고, 그 수를 출력한다.만약 스택에 들어있는 정수가 없는 경우에는 - 1을 출력한다.
//size : 스택에 들어있는 정수의 개수를 출력한다.
//empty : 스택이 비어있으면 1, 아니면 0을 출력한다.
//top : 스택의 가장 위에 있는 정수를 출력한다.만약 스택에 들어있는 정수가 없는 경우에는 - 1을 출력한다.

int main()
{
	int N, tmp;
	string str;
	cin >> N;

	while (N--)
	{
		cin >> str;

		if (str == "push")
		{
			cin >> tmp;

			Stack[++top] = tmp;
		}
		else if (str == "pop")
		{
			if (top == -1)
			{
				cout << -1 << '\n';
			}
			else
			{
				cout << Stack[top--] << '\n';
			}
		}
		else if (str == "size")
		{
			cout << top + 1 << '\n';
		}
		else if (str == "empty")
		{
			if (top == -1)
				cout << 1 << '\n';
			else
				cout << 0 << '\n';
		}
		else
		{
			if (top == -1)
				cout << -1 << '\n';
			else
				cout << Stack[top] << '\n';
		}
	}

}